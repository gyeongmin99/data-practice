# 데이터 분석 사고의 핵심 패턴
    # 패턴 1: 값 -> 분포 -> 맥락
        # (1) 값들은 얼마인가? (계산)
        # (2) 값들이 어떻게 흩어져 있는가? (분포)
        # (3) 이 분포가 어떤 상황에서 나왔는가? (맥락)
            ## point!
                ## 평균을 보면 반드시 분포를 본다
                ## 분포를 보면 반듯 맥락을 본다
    # 패턴 2: 존재하는 행동보다 '사라진 행동'을 보기
        # 무엇을 했는가?가 아닌, 무엇을 하지 않았는가?에 초점
            # (예) 클릭 수보다 클릭하지 않은 사용자
            # (예) 평균 사용 시간보다 0인 세션
            # -> '0, 공백, 이탈, 중단'은 가장 강력한 분석 신호
    # 패턴 3: 요약 통계와 개별 사례를 왕복하기
        # 이 평균은 어떤 개별 사례들로 만들어졌는가?
        # 이 특이값(outlier)은 전체 해석을 왜곡하는가, 설명하는가?
    # 패턴 4: 수치 -> 시각 -> 문장
        # 문장으로 설명할 수 있을 때 분석의 완성
        # "그래서 이 수치는 무엇을 의미하는가?"
    # 패턴 5: 설명 가능한 해석한 한다
        # "이 데이터 범위 내에서 말할 수 있는 것은 ~이다"


